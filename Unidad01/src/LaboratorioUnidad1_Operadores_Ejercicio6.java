
public class LaboratorioUnidad1_Operadores_Ejercicio6 {

	public static void main(String[] args) {
		
		int n1 = 5, n2 = 10, n3 = 20;
		
		System.out.println("n1 = " + n1);
		System.out.println("n2 = " + n2);
		System.out.println("n3 = " + n3);
		
		int suma = n1 + n2;
		
		int resta = n3 - n1;
		
		int mult = n1 * n3;
		
		int div = n3 / n2;
		
		System.out.println("\nn1 + n2 = " + suma);
		System.out.println("n3 - n1 = " + resta);
		System.out.println("n1 * n3 = " + mult);
		System.out.println("n3 / n2 = " + div);

	}

}
