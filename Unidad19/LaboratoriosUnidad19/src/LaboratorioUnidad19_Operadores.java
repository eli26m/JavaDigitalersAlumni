
public class LaboratorioUnidad19_Operadores {

	public static void main(String[] args) {
		
		// Punto 1
			int num1 =10, num2 = 5, res;
				
			res = num1 + num2;
			System.out.println("num1 + num2 = " + res);
				
			res = num1 - num2;
			System.out.println("num1 - num2 = " + res);
				
			res = num1 * num2;
			System.out.println("num1 * num2 = " + res);
				
			res = num1 / num2;
			System.out.println("num1 / num2 = " + res);
				
				
		// Punto 2
			int var1 = 4, var2 = 6, var3 = 8, var4 = 10, suma, promedio;
				
			suma = var1 + var2 + var3 + var4;
			promedio = suma / 4;
				
			System.out.println("\nPromedio = " + promedio);
	}

}
